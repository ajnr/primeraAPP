¿Qué es el Cloud Computing?
Es un modelo de entrega y consumo de servicios, es una manera de acceder a unos recursos o capacidades para beneficio de una empresa o individuo, NO es una tecnología. 
Hay un consumidor y un prestador, el consumidor busca recursos ya que no los produce, por ello lo busca en un prestador y busca acceder de manera directa que suele ser a través de un navegador, internet o app móvil. No se debe intermediar con humanos, de frente en la web y empezar a consumir el producto.
Se paga por uso, por consumo en función a la demanda.
Cloud Computing necesita tecnología para ser implementada y entregada, pero NO es tecnología, es un modelo de relación.
Palabras clave del Cloud Computing: Flexibilidad, disponibilidad, agilidad, pago por uso, seguridad y control en funcionen de los prestadores.
¿Qué supone para el cliente final (consumidor)?
El que consume no participa en la creación o producción del servicio que está consumiendo, no tiene el control de lo que pasa en la producción y tiene que asumir las condiciones de consumo publicadas por el prestador de servicios, leer bien las condiciones de uso, las condiciones del contrato, saber que está consumiendo y quien es el responsable, cuánto cuesta lo que está consumiendo, las condiciones de seguridad y continuidad y recién ahí firmar.
Se generan ciertos problemas de adopción por el usuario por que no acepta su rol, ya que se quiere meter en la producción o cadena de suministro, NO debería entrar ni replicar el modelo. 
El Cloud conlleva industria y automatización.
A veces se llegan acuerdos particulares, pero muy raras veces.
¿Qué se supone para el prestador?
Es el que afronta la fuerte inversión, automatiza (el consumidor busca acceder al servicio sin ninguna mano humana, el prestador debe estar con los recursos necesarios para responder ante eso), gobernanza (saber que está ofreciendo y rendimiento, calidad del servicio, cumplimiento con los compromisos, costes) y control. Repone los productos según se va consumiendo.
En Cloud Computing siempre que no sea gratuito, el usuario paga por el servicio Cloud unas tarifas determinadas en función de la demanda que hace y de lo que va consumiendo.
