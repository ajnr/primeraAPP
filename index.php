•	Se invierte mucho en perímetros de seguridad, cosa que cada vez existe menos y solo dejan poca inversión en el resto.
•	Al final se podrá acceder a la información que esta perimetrada y será la inversión en vano.
•	Se tiene que cambiar la proximidad en el tema se seguridad
•	En Cloud se esfuerzan en la seguridad
•	En el área de seguridad se intenta ofrecer tecnología y servicio que integran donde se tiene la información (plataformas propias o de terceros) para que se pueda aplicar la política de seguridad de manera más integradora.
•	Microsegmentación: aplica seguridad dentro del perímetro, es decir, que no solo se resguarda el perímetro, sino todo lo que hay dentro de ella, TODO. Pero no se puede hacer de golpe, se tiene que automatizar para que sea viable. Se define por software que permite granularidad aplicada a cada átomo de la plataforma este donde este (dispositivo, pc, en el mundo cloud, etc) con una gestión unificadora, centralizada e integradora; se podrá llegar a todas las partes con más solvencia.
•	Es una capa y hay que buscar la manera de buscar y evitar la intrusión.
•	En el mundo cloud donde se despliega va la política de seguridad porque está definida por software.
•	Dada la dispersión de prestadores y plataformas cloud, la seguridad debe definirse y aplicarse por modelos integrados y unificados.
