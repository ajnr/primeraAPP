Profundizamos HDDC vs SDDC
La decisión sobre HDDC o SDDC depende del tipo de cargas a alojar y su evolución.
•	SDDC: CENTRO DE DATOS DEFINIDO POR SOFTWARE: utiliza un hardware genérico
•	HDDC: CENTRO DE DATOS DEFINIDO POR HARDWARE: para requerimientos puntuales o características técnicas claves.
•	La infraestructura debe ser los más genérica posible, debe ser fácil de operar, de consumir, de encontrar, expandir y hacer crecer, se debe enfocar en las capas más cercanas al servicio.
•	Software define utiliza un hardware genérico


